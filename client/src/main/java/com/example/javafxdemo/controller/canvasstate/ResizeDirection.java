package com.example.javafxdemo.controller.canvasstate;

/**
 * Enum representing the direction in which a component can be resized.
 */
public enum ResizeDirection {
    LEFT,          // Resize the component horizontally on the left side
    RIGHT,         // Resize the component horizontally on the right side
    TOP,           // Resize the component vertically on the top side
    BOTTOM,        // Resize the component vertically on the bottom side
    TOP_LEFT,      // Resize the component diagonally on the top-left corner
    TOP_RIGHT,     // Resize the component diagonally on the top-right corner
    BOTTOM_LEFT,   // Resize the component diagonally on the bottom-left corner
    BOTTOM_RIGHT   // Resize the component diagonally on the bottom-right corner
}
